const a = 7
let b = 3
// Atruição aditiva
b += a // b = b + a
console.log(b)

// Atribuição Subtrativa
b -= 4 // b = b - 4
console.log(b)

// Atribuiçao Multiplicativa
b *= 2 // b = b * 2
console.log(b)

// Atribuiçao divisiva
b /= 2 // b = b / 2
console.log(b)

// Atribuiçao modula
b %= 2 // b = b % 2  --> resto da divisão
console.log(b)

// Saída:

// 10
// 6
// 12
// 6
// 0